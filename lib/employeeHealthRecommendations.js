/**
 * คำแนะเชิงองค์กรจากค่า BMI / ความดัน / น้ำตาล + แนวทางการทำงานทั่วไป
 * ไม่ใช่การวินิจฉัยโรค — ใช้สื่อสารป้องกันและส่งต่อทางการแพทย์เมื่อจำเป็น
 * ใช้ถ้อยคำเป็นกลาง ไม่ระบุภารกิจย่อยของแต่ละแผนก เพื่อไม่ให้เกิดความรู้สึกถูกมองแคบตามหน้าที่
 */

/** เก็บไว้สำหรับขยายในอนาคต (เช่น รายงานสถิติตามกลุ่มงาน) — ตอนนี้คำแนะใน UI ใช้ชุดเดียวกันทุกแผนก */
export function getDepartmentWorkProfileKey(department) {
  const d = String(department || "").trim();
  if (/ช่าง|ช่างไฟ|ช่างยนต์|ช่างกล/.test(d)) return "engineering";
  if (/สาธารณสุข|สุขา|สุขาภิบาล/.test(d)) return "sanitation";
  if (/ประปา|น้ำประปา|การประปา/.test(d)) return "waterworks";
  return "general";
}

/** แนวทางเดียวกันทุกแผนก — เน้นมาตรฐานความปลอดภัยและอนามัยการทำงานทั่วไป */
const NEUTRAL_OCCUPATIONAL_TIPS = [
  "จัดเวลาพักและดื่มน้ำสะอาดสม่ำเสมอ โดยเฉพาะเมื่อทำงานกลางแจ้งหรือในอากาศร้อน",
  "สลับท่าทาง พักยืดเหยียดระหว่างวัน และใช้อุปกรณ์ช่วยยกหรือเครื่องมือให้เหมาะสมกับน้ำหนักและลักษณะของงาน",
  "สวมใส่รองเท้าและอุปกรณ์ป้องกันตามที่หน่วยงานกำหนด และระมัดระวังพื้นลื่น สิ่งกีดขวาง หรือยานพาหนะเมื่อออกพื้นที่",
  "ปฏิบัติตามมาตรฐานความปลอดภัยในการทำงานของหน่วยงาน และทำงานเป็นคู่หรือแจ้งตำแหน่งเมื่ออยู่ในพื้นที่จำกัด",
];

function pushUnique(arr, line) {
  const t = String(line || "").trim();
  if (!t) return;
  if (!arr.includes(t)) arr.push(t);
}

export function buildEmployeeHealthRecommendations(person) {
  const out = [];

  pushUnique(out, "การดูแลระหว่างปฏิบัติงาน (แนวทางทั่วไป):");
  for (const tip of NEUTRAL_OCCUPATIONAL_TIPS) pushUnique(out, tip);

  const bmiCat = person?.bmiCategory;
  const bpCat = person?.bp?.category;
  const sugarCat = person?.sugarCategory;

  pushUnique(out, "— จากผลตรวจเบื้องต้นในระบบ —");

  if (bmiCat === "underweight") {
    pushUnique(out, "BMI ต่ำกว่าเกณฑ์: เน้นอาหารให้พลังงานเพียงพอ โดยเฉพาะวันที่มีการใช้ร่างกายมาก — หากผอมลงเร็วควรปรึกษาแพทย์");
  } else if (bmiCat === "overweight" || bmiCat === "obese1" || bmiCat === "obese2") {
    pushUnique(out, "BMI สูงกว่าเกณฑ์: ค่อยๆ ปรับอาหารลดหวานมันเค็ม เพิ่มการเคลื่อนไหวที่ปลอดภัยกับข้อและหลัง — เหมาะกับการติดตามน้ำหนักเป็นระยะ");
  } else if (bmiCat === "normal") {
    pushUnique(out, "BMI อยู่ในเกณฑ์ที่ระบบกำหนดว่าปกติ: รักษาน้ำหนักและความแข็งแรงของกล้ามเนื้อให้เหมาะกับลักษณะงาน");
  } else {
    pushUnique(out, "BMI ไม่ครบข้อมูล: ตรวจวัดน้ำหนัก–ส่วนสูงให้ครบในครั้งถัดไปเพื่อประเมินซ้ำ");
  }

  if (bpCat === "high") {
    pushUnique(out, "ความดันสูงตามเกณฑ์คัดกรอง: พักก่อนวัดซ้ำ ลดเค็ม งดสูบบุหรี่ จัดการความเครียด — หากสูงซ้ำหรือมีอาการเจ็บหน้าอก หายใจลำบาก ให้รีบพบแพทย์");
  } else if (bpCat === "risk") {
    pushUnique(out, "ความดันอยู่ระดับควรติดตาม: นัดวัดซ้ำตามโปรแกรมหน่วยงาน และปรับพักผ่อน–อาหาร");
  } else if (bpCat === "low") {
    pushUnique(out, "ความดันต่ำกว่าเกณฑ์: ดื่มน้ำ พักเมื่อมึนศีรษะหรือหน้ามืด โดยเฉพาะหลังอยู่กลางแจ้งนาน");
  } else if (bpCat === "normal") {
    pushUnique(out, "ความดันอยู่ในเกณฑ์ที่ระบบกำหนดว่าปกติ: รักษาพฤติกรรมดีต่อเนื่อง");
  } else {
    pushUnique(out, "ความดันไม่ครบข้อมูล: วัดซ้ำหลังนั่งพัก 5–10 นาทีในสภาพสงบ");
  }

  if (sugarCat === "high") {
    pushUnique(out, "น้ำตาลในเลือดสูงตามเกณฑ์คัดกรองในระบบ: ควรส่งต่อพบแพทย์/ตรวจน้ำตาลซ้ำตามบริบท (อดอาหาร/หลังอาหาร) เพื่อยืนยัน");
  } else if (sugarCat === "risk") {
    pushUnique(out, "น้ำตาลอยู่ช่วงเสี่ยง: ลดหวานน้ำอัดลม ออกกำลังสม่ำเสมอ และติดตามซ้ำ");
  } else if (sugarCat === "low") {
    pushUnique(out, "น้ำตาลต่ำตามเกณฑ์ในระบบ: ระวังอาการมือสั่น เหงื่อออก หากเกิดหลังใช้แรงมากให้พักและรับประทานอาหารตามคำแนะแพทย์");
  } else if (sugarCat === "normal") {
    pushUnique(out, "น้ำตาลอยู่ในเกณฑ์ที่ระบบกำหนดว่าปกติ");
  } else {
    pushUnique(out, "น้ำตาลไม่ครบข้อมูล: ระบุเวลาตรวจและเงื่อนไขก่อนตรวจให้สม่ำเสมอในครั้งถัดไป");
  }

  pushUnique(out, "— สรุปภาพรวม —");
  if (person?.overallRisk) {
    pushUnique(
      out,
      "มีอย่างน้อยหนึ่งค่าที่เข้าเกณฑ์เสี่ยง/ผิดปกติในระบบ — แนะนำให้หัวหน้างานหรือเจ้าหน้าที่สุขภาพช่วยประสานการส่งต่อหรือนัดติดตาม"
    );
  } else {
    pushUnique(
      out,
      "ค่าหลักในระบบอยู่ในเกณฑ์ที่กำหนดว่าปกติ — ยังคงตรวจสุขภาพประจำปีและดูแลความปลอดภัยในการทำงานต่อไป"
    );
  }

  return out.filter(Boolean);
}
